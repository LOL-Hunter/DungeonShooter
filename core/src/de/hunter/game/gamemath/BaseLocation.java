package de.hunter.game.gamemath;

public class BaseLocation {
    protected int x;
    protected int y;

    public int getX(){
        return this.x;
    }
    public int getY() {
        return y;
    }
    public void setX(int x) {
        this.x = x;
    }
    public void setY(int y) {
        this.y = y;
    }
}
